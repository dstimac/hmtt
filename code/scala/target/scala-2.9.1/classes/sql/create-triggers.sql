  REFERENCING NEW ROW AS newrow
  FOR EACH ROW
  BEGIN ATOMIC
     INSERT INTO PLAYERHISTORY VALUES(
       newrow.ID,
       CURRENT_TIMESTAMP,
       newrow.FIRSTNAME,
       newrow.LASTNAME,
       newrow.AGE,
       newrow.AGEDAYS,
       newrow.TSI,
       newrow.FORM,
       newrow.STAMINA,
       newrow.EXPERIENCE,
       newrow.LEADERSHIP,
       newrow.SALARY,
       newrow.SPECIALITY,
       newrow.GOALKEEPING,
       newrow.PLAYMAKING,
       newrow.SCORING,
       newrow.PASSING,
       newrow.DEFENDING,
       newrow.WINGER,
       newrow.SETPIECES);

       INSERT INTO LASTSKILLUPDATE VALUES(
       newrow.ID,
       CURRENT_TIMESTAMP,
       CURRENT_TIMESTAMP,
       CURRENT_TIMESTAMP,
       CURRENT_TIMESTAMP,
       CURRENT_TIMESTAMP,
       CURRENT_TIMESTAMP,
       CURRENT_TIMESTAMP,
       CURRENT_TIMESTAMP,
       CURRENT_TIMESTAMP);
   END

CREATE TRIGGER UPDATEPLAYERTRIGG AFTER UPDATE on PLAYER
  REFERENCING OLD ROW AS oldrow NEW ROW AS newrow
  FOR EACH ROW
  BEGIN ATOMIC
     INSERT INTO PLAYERHISTORY VALUES(
       newrow.ID,
       CURRENT_TIMESTAMP,
       newrow.FIRSTNAME,
       newrow.LASTNAME,
       newrow.AGE,
       newrow.AGEDAYS,
       newrow.TSI,
       newrow.FORM,
       newrow.STAMINA,
       newrow.EXPERIENCE,
       newrow.LEADERSHIP,
       newrow.SALARY,
       newrow.SPECIALITY,
       newrow.GOALKEEPING,
       newrow.PLAYMAKING,
       newrow.SCORING,
       newrow.PASSING,
       newrow.DEFENDING,
       newrow.WINGER,
       newrow.SETPIECES);

     IF (oldrow.STAMINA < newrow.STAMINA) THEN UPDATE LASTSKILLUPDATE SET STAMINA = CURRENT_TIMESTAMP WHERE newrow.ID = PLAYERID ; END IF;
     IF (oldrow.EXPERIENCE < newrow.EXPERIENCE) THEN UPDATE LASTSKILLUPDATE SET EXPERIENCE = CURRENT_TIMESTAMP WHERE newrow.ID = PLAYERID; END IF;
     IF (oldrow.GOALKEEPING < newrow.GOALKEEPING) THEN UPDATE LASTSKILLUPDATE SET GOALKEEPING = CURRENT_TIMESTAMP WHERE newrow.ID = PLAYERID; END IF;
     IF (oldrow.PLAYMAKING < newrow.PLAYMAKING) THEN UPDATE LASTSKILLUPDATE SET PLAYMAKING = CURRENT_TIMESTAMP WHERE newrow.ID = PLAYERID; END IF;
     IF (oldrow.SCORING < newrow.SCORING) THEN UPDATE LASTSKILLUPDATE SET SCORING = CURRENT_TIMESTAMP WHERE newrow.ID = PLAYERID; END IF;
     IF (oldrow.PASSING < newrow.PASSING) THEN UPDATE LASTSKILLUPDATE SET PASSING = CURRENT_TIMESTAMP WHERE newrow.ID = PLAYERID; END IF;
     IF (oldrow.DEFENDING < newrow.DEFENDING) THEN UPDATE LASTSKILLUPDATE SET DEFENDING = CURRENT_TIMESTAMP WHERE newrow.ID = PLAYERID; END IF;
     IF (oldrow.WINGER < newrow.WINGER) THEN UPDATE LASTSKILLUPDATE SET WINGER = CURRENT_TIMESTAMP WHERE newrow.ID = PLAYERID; END IF;
     IF (oldrow.SETPIECES < newrow.SETPIECES) THEN UPDATE LASTSKILLUPDATE SET SETPIECES = CURRENT_TIMESTAMP WHERE newrow.ID = PLAYERID; END IF;
  END
